package Day02_VariablesAndString;

public class Topic3_Examples {
    public static void main(String[] args) {
        // Type VariableName = Value;

        int Year = 2022;
        int Month = 12;

        System.out.println(Year);
        System.out.println(Month);

        // Zaten yukarda tanitildigi icn asagida tekrardan int ile giris yapilmaz. Zaten sene dedik.

        Year = 2023;
        Month = 1;

        System.out.println(Year);
        System.out.println(Month);
    }
}
