package Day10_NestedLoops;

public class Topic3_TS_doWhileLoop {
    public static void main(String[] args) {


        /*
        do while loop is another loop type
        type of loops: loop, while loop and do while loop
         */
    int i = 1;
    do {
        System.out.println("i = " + i);
        i++;
    } while (i<=6);
    }
}
